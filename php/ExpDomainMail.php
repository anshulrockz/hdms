    $mail->Password = '9720044889';                           // SMTP password
    $mail->SMTPSecure = 'ssl';                            // Enable TLS encryption, `ssl` also accepted
    $mail->Port = 465;                                    // TCP port to connect to

    //Recipients
    $mail->setFrom('anshul.agrawal889@gmail.com', 'Techstreet');
    $mail->addAddress('anshul.smarty16@gmail.com', 'Anshul Agrawal');     // Add a recipient
    $mail->addAddress('anshul.smarty16@gmail.com');               // Name is optional
    $mail->addReplyTo('anshul.agrawal889@gmail.com', 'Techstreet');
    //$mail->addCC('');
    //$mail->addBCC('');

    //Attachments
    //$mail->addAttachment('/var/tmp/file.tar.gz');         // Add attachments
    //$mail->addAttachment('/tmp/image.jpg', 'new.jpg');    // Optional name

    //Content
    $mail->isHTML(true);                                  // Set email format to HTML
    $mail->Subject = 'Domain expiring after 1 month';
    $mail->Body    = $bodyText;
    $mail->AltBody = 'This is the body in plain text for non-HTML mail clients';

    $mail->send();
    echo 'Message has been sent';
